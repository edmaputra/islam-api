package com.edmaputra.waktushalat.model;

public class Shalat {

    private String shalat;
    private String waktu;

    public String getShalat() {
        return shalat;
    }

    public void setShalat(String shalat) {
        this.shalat = shalat;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }
}
